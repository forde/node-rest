const express = require('express');
const router = express.Router();

const Order = require('./../models/order');

// '/' === '/orders/'

/*
|--------------------------------------------------------------------------
| List all orders
|--------------------------------------------------------------------------
*/
router.get('/', Order._list);

/*
|--------------------------------------------------------------------------
| Create order
|--------------------------------------------------------------------------
*/
router.post('/', Order._create);

/*
|--------------------------------------------------------------------------
| Get single order
|--------------------------------------------------------------------------
*/
router.get('/:id', Order._get);

/*
|--------------------------------------------------------------------------
| Delete order
|--------------------------------------------------------------------------
*/
router.delete('/:id', Order._delete);

module.exports = router;