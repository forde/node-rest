const express = require('express');
const router = express.Router();

const User = require('./../models/user');

// '/' === '/user/'

/*
|--------------------------------------------------------------------------
| Sign up user
|--------------------------------------------------------------------------
*/
router.post('/signup', User._signup);

/*
|--------------------------------------------------------------------------
| Sign in user
|--------------------------------------------------------------------------
*/
router.post('/signin', User._signin);

/*
|--------------------------------------------------------------------------
| Delete user
|--------------------------------------------------------------------------
*/
router.delete('/:id', User._delete);


module.exports = router;